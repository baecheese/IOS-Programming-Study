- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"WeatherCell" forIndexPath:indexPath];
    
    cell.textLabel.text = [[[DataCenter defaultData]dataForWeather:self.weatherType]objectAtIndex:indexPath.row];
    
    return cell;
}



